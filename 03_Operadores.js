/* Operadores de Comparacion
    > Mayor que
    < Menor que
    >= Mayor o igual que
    <= Menor o igual que
    == Igualdad (solo valor)
    != Desigualdad (solo valor)
    === Igualdad estricta (valor y tipo)
    !== Desigualdad estricta (valor y tipo)
*/

console.log(3 + 2); //5
console.log(3 - 2); //1
console.log(3 * 2); //6
console.log(3 / 2); //1.5
console.log(3 % 2); //1
console.log(3 ** 2); //9    
console.log(3 > 2); //true
console.log(3 < 2); //false
console.log(3 >= 2); //true
console.log(3 <= 2); //false
console.log(3 == 2); //false
console.log(3 != 2); //true
console.log(3 == "3"); //true
console.log("-------");
console.log(100 == "100"); //true
console.log(100 === "100"); //false
console.log(100 !== "100"); //true  
console.log(100 !== 100); //false

