// Bucle for
// variable de control, condición, incremento

for (var i = 1; i <= 30; i++) {
    console.log("El valor de i es: " + i);
}

console.log("--------------------------");

// Bucle while

// variable de control
var j = 1;
// condición
while (j < 5 ) {
    console.log(j);
    // incremento
    j++;
}