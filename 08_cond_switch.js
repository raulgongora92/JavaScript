// Condicional switch
// Ejemplo de uso de switch para manejar m√∫ltiples condiciones

var place = "second";

switch (place) {
    case "first":
        console.log("Gold");
        break;
    case "second":
        console.log("Silver");
        break;
    case "third":
        console.log("Bronze");
        break;
    default:
        console.log("No medal awarded");
}